// ---------------------------------------------------------------------------
// Template file definitions
// ---------------------------------------------------------------------------

export interface TemplateFile {
  path: string
  content: string
}

export interface TemplateDefinition {
  id: string
  files: (projectName: string) => TemplateFile[]
}

// ---------------------------------------------------------------------------
// Shared README (all templates use the same structure)
// ---------------------------------------------------------------------------

function readmeContent(projectName: string): string {
  return `# ${projectName}

An [ADV.JS](https://advjs.org) visual novel project.

## Structure

\`\`\`
adv/
├── world.md          # 世界观设定
├── outline.md        # 故事大纲
├── chapters/         # 章节文件
│   └── 01.adv.md     # 第一章
└── characters/       # 角色设定（可选）
\`\`\`

## Getting Started

Edit files in \`adv/\` to create your story. Use [AdvScript](https://advjs.org/guide/advscript/) syntax in \`.adv.md\` files.

## Preview

Run with ADV.JS CLI:

\`\`\`bash
npx advjs dev
\`\`\`
`
}

